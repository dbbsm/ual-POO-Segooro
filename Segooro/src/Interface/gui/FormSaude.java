    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean existeT=false;
        try
        {
            for(Titular T : emp)
            {
                if(T.getIDtitular()==Integer.parseInt(jTextField4.getText()))
                {
                    existeT=true;
                    try
                    {
                        if(T.getIdade()<17)
                        {

                            if(Double.parseDouble(jTextField5.getText())==100)
                            {
                                T.criarSeguroSaude(Double.parseDouble(jTextField5.getText()));
                                JOptionPane.showMessageDialog(null, "O seu seguro foi criado com sucesso.");

                            }
                            else
                                throw new ValorBaseJuniorNaoAtingidoException("O valor do Seguro de "
                                        + "Saude Junior tem um PreÃ§o fixo de 100");
                        }
                        else if(T.getIdade()>=17 && T.getIdade()<=80)
                        {
                            if(Double.parseDouble(jTextField5.getText())>=150)
                            {
                                T.criarSeguroSaude(Double.parseDouble(jTextField5.getText()));
                                JOptionPane.showMessageDialog(null, "O seu seguro foi criado com sucesso.");

                            }
                            else
                                throw new ValorBaseAdulto_SeniorNaoAtingidoException("O Valor Base do "
                                        + "Seguro de Saude Adulto e Senior e de 150");
                        }
                        else if(T.getIdade()>80)
                        {
                            throw new IdadeDoTitularNaoPodeTerSeguroSaudeException("O titular "
                                    + "Introduzido nao pode criar um seguro de saude pois "
                                    + "ja tem mais de 80 anos.");
                        }
                    }
                    catch(NumberFormatException e)
                    {
                        JOptionPane.showMessageDialog(null, "O valor do Preco introduzido nao e valido.");
                    }
                    
                }
            }
            if(existeT==false)
                throw new TitularNaoExisteException("O ID do titular nao existe.");
        }
        catch(TitularNaoExisteException |ValorBaseJuniorNaoAtingidoException | ValorBaseAdulto_SeniorNaoAtingidoException | IdadeDoTitularNaoPodeTerSeguroSaudeException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        Limpar();
    }//GEN-LAST:event_jButton1ActionPerformed


